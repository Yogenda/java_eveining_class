public class Nest1 {

	public static void main(String[] args) {
		//1 2 3
		//1 2 3
		//1 2 3
		int i =1;
		while(i<=3) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
		i =1;
		while(i<=3) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
		i =1;
		while(i<=3) {
			System.out.print(i+" ");
			i++;
		}	
	}
}
