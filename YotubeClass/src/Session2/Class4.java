package Session2;

public class Class4 {
	public static void main(String[] args) {
		int x = 10;
		int y = 3;
		
		System.out.println("less then: "+(x<y));
		System.out.println("greater then: "+(x>y));
		System.out.println("greater equal then: "+(x>=y));
		System.out.println("less equal then: "+(x<y));
		System.out.println("Not equal then: "+(x!=y));
		System.out.println("less equal then: "+(x==y));
	}
}
